'''Lily Rose Beatrice Allen'''<ref name="bmi rep">{{cite web| url = http://repertoire.bmi.com/writer.asp?page=1&blnWriter=True&blnPublisher=True&blnArtist=True&fromrow=1&torow=25&affiliation=PRS&cae=505690654&keyID=1041457&keyname=ALLEN%20LILY%20ROSE%20BEATRICE&querytype=WriterID| title = Songwriter/Composer: ALLEN LILY ROSE BEATRICE| accessdate = 2007-02-17| work = Repertoire Search| publisher = [[Broadcast Music Incorporated]]| language= [[English language|English]]}}</ref> (born [[May 2]], [[1985]]) is an Brit Award nominated [[England|English]] [[Pop music|pop]] artist, currently signed to [[Regal Records (1914)|Regal]]/[[Parlophone]] ([[EMI]]), and known for songs such as "[[Smile (Lily Allen song)|Smile]]" and "[[LDN (song)|LDN]]". She is the daughter of actor/comedian/musician [[Keith Allen]] and film producer [[Alison Owen]]. Her single "Smile" reached #1 on the [[UK singles charts]] in July 2006.

==Life==
Lily Allen was born in [[Hammersmith]], London. She eventually settled in the North [[London Borough of Islington]]. 
She has an older sister, Sarah; a younger brother, [[Alfie Owen-Allen]] (who was the subject of her song "[[Alfie (Lily Allen song)|Alfie]]"); and a number of [[half-sibling]]s, including her half-sister, Teddie. Allen has also lived with her mother in [[Leixlip]], [[County Kildare]], [[Ireland]] where she went to Scoil Bhride, a local primary school. 

Allen was accused by some of cultivating a [[chav]] image, but she actually enjoyed a highly privileged upbringing, attending the [[private school]]s [[Hill House School|Hill House]] and [[Bedales School|Bedales]], famous as one of the five most expensive schools in the UK.<ref>{{cite news | last = Walker | first = Andrew | title = Faces of the week | publisher = BBC News | date = [[2006-07-21]] | url = http://news.bbc.co.uk/1/hi/magazine/5202534.stm | accessdate = 2006-07-26}}</ref> Allen changed schools 13 times during her childhood and was expelled from several of them.<ref>{{cite news | last = Sawyer | first = Miranda | title = Pictures of lily | publisher = Observer | date = [[2006-05-21]] | url = http://observer.guardian.co.uk/omm/story/0,,1776732,00.html | accessdate = 2006-10-04}}</ref> When asked about it on the Jo Whiley show on BBC Radio 1, she explained "Education is great for some people, but for me it just wasn't."<ref>(2006-11-27) ''Jo Whiley Live Lounge Tour'' [Radio Show].</ref>
    
While holidaying in [[Ibiza]], Allen was introduced to [[A&R]] man George Lamb, former manager of [[Audio Bullys]]. He introduced Allen to music producers [[Future Cut]], who wrote tracks for lyrics she had written.

It has been said that she grew up alongside T4 Presenter [[Miquita Oliver]] and that the pair are best friends.<ref>http://www.findarticles.com/p/articles/mi_qn4153/is_20061006/ai_n16773652</ref>

Allen was still in the process of finding her songwriting voice, stating that, "I think the writing is quite literal and observational because I haven’t developed any process, so to speak, of writing. It feels too much like work to me.”<ref>{{cite web|url=http://www.ukula.com/TorontoArticle.aspx?SectionID=2&ObjectID=1714&CityID=3 |title=Profile at Ukula}}</ref>

She was romantically linked to A&R man Seb Chew who described her in [[The Independent]] newspaper as the one who got away.

In February 2007 British fashion chain [[New Look (store)|New Look]] has announced that Lily Allen will design a range of clothes for them. The range, titled "Lily Loves," will feature six dresses, two pairs of shoes and some brightly colored accessories. Clothes designed by her will be available in stores in May.<ref>[http://www.catwalkqueen.tv/2007/02/lily_allen_to_d.html Lily Allen to design for New Look] CatwalkQueen, February, 2007</ref>

==Regal Records and MySpace==
In late 2005, after a short-lived deal with [[Warner Music]] signed in 2002 and before releasing any music (a decision influenced by her father), Allen signed with [[Regal Records (1914)|Regal Records]] (part of [[Parlophone]]-[[EMI]]). Despite alleged resistance from the label, who wanted to send her off to a "hit factory" to record her album, she uploaded the tracks she had written (songs that had her signed in the first place) to community website [[MySpace]] in November 2005, regularly updating the developments in her life. 

More and more people discovered her profile, with links to her tracks being e-mailed and eventually reaching UK music publication ''[[NME]]'', reviews site [[musicOMH.com]], and online music blog [[Popjustice]]. These publications reviewed her and her demo tracks positively. After this exposure, which came well before the proposed album release date, ''[[The Observer]]'' published a short report on Allen; soon after, the demo of her track "LDN" was played on [[BBC Radio One]] by DJ [[Jo Whiley]]. Because of the consequent buzz, her release dates were changed, and "LDN", produced by her, was released—on limited-edition [[7" vinyl]] only—as Allen's first single, on [[24 April]] [[2006]]. This was accompanied by album track "Knock 'Em Out" on the [[A-side and B-side|B-side]]. She also took a spot at the [[Notting Hill Arts Club]], [[London]], in early May, performing every Thursday night for four weeks.

==''Alright, Still''==
Allen's debut album, ''[[Alright, Still]]'', was released as a limited-edition 12" vinyl in the UK on [[July 3]], [[2006]]; the full CD release followed in the UK and the rest of Europe on [[July 17]], [[2006]]. The album features between 11 and 14 [[Song|tracks]] (depending on the copy of the CD), most of which were previewed on her [http://www.myspace.com/lilymusic MySpace] page, including the single "Smile" (the first song she ever wrote with Future Cut), "LDN", "Knock 'Em Out", and "Alfie". "Friday Night", co-written with [[Jonny Bull]], is also included, alongside "Littlest Things", produced by [[Mark Ronson]]. The album was released on [[January 30]], [[2007]] in the [[United States]], selling 34,000 copies to land #20 on the Billboard Album Charts.[http://www.mtv.com/news/articles/1551773/20070207/jones_norah.jhtml] The US version of the album has been remixed and sounds substantially different than the original UK release.

In September 2006, "Smile" was made available on the United States version of the [[Apple Inc.]]'s [[iTunes Music Store]]. As of December 2006, her music video for "Smile" has been played on various music channels as well as the song getting a little airplay. She has also done several promotional ads for [[MTV]] as their ''Discover and Download'' artist of the month for January.  

Entertainment Weekly named "Alright, Still" as one of the top 10 albums of 2006 despite the fact that it had not yet been released in the U.S.  The album was released in the [[United States]] on [[30 January]] [[2007]], with "Smile" serviced to radio in January as the first single.  The U.S. version contains two additional tracks:  "Nan, You're A Window Shopper" and a new remix of "Smile."

On [[November 30]], [[2006]] she appeared as a guest on the [[BBC2]] comedy quiz show [[Never Mind the Buzzcocks]].

On February 3, Allen enjoyed her first stateside television performance on ''[[Saturday Night Live]]'' hosted by [[Drew Barrymore]].  She performed the songs "Smile" and "LDN."

In [[concert]], Allen performed a [[parody]] of [[50 Cent]]'s "[[Window Shopper]]", called "Nan, You're a Window Shopper", which was commercially released as a B-side to "LDN", because approval was not given in time for the ''Alright, Still'' release.  Nonetheless, the song appears on the U.S. version of Alright, Still.

She also provides backing vocals on the [[Basement Jaxx]] track "Lights Go Down" which appears on their fourth studio album, ''[[Crazy Itch Radio]]'' and appears on tracks on [[Robbie Williams]]'s latest album, ''[[Rudebox (album)|Rudebox]]''.

Confirmed for release, the 4th single from the album "Alright, Still" will be a Double A-side of "[[Shame For You]]"/"[[Alfie (Lily Allen song)|Alfie]]".

On the week ending on [[January 28]], [[2007]], British artists made chart history taking all Top 10 places in the Official UK Albums chart for the first time since the chart was established in 1956, according to British record labels trade association the BPI, the album "Alright, Still" was number-nine that week.<ref>[http://www.bpi.co.uk/index.asp?Page=news/press/news_content_file_1056.shtml BPI Official Website, UK talent celebrates first all-British Top 10 in UK chart history]</ref>

A Simlish version of Lily Allen's hit single "Smile" is featured in the The Sims 2: Seasons expansion pack. EA Games have also launched a singing competition on the website SingShot where Sims players have to record themself singing along to the song in Simlish.

==Controversies==
<!-- Unsourced image removed: [[Image:Lilyallenphoto.jpg|right]] -->
Allen has been the subject of many controversies, mainly because of her expressed opinions of other musicians. She went to private school with [[Luke Pritchard]], of [[The Kooks]]; made some disparaging comments about them; and then covered their song "Naive" on [[Jo Whiley]]'s Live Lounge to show there were "no hard feelings". She has also called [[Bob Geldof]] a "cunt"<ref>{{cite web | url = http://www.myspace.com/lilymusic | title = Lily Allen MySpace Profile | accessdate = 2006-07-24 | last = Allen | first = Lily }}</ref> and a "sanctimonious prat",<ref>{{ cite visual | title = Transmission with T-Mobile | date = 2006-06-17 | medium = TV-Show }}</ref> and has claimed that [[Kylie Minogue]] performing at the [[Glastonbury Festival]] would be the ultimate insult to the event.<ref>{{ cite web | url = http://www.contactmusic.com/news.nsf/article/allen%20slams%20kylie%20glastonbury%20appearance_1002220 | title = Allen Slams Kylie Glastonbury Appearance | date = 2006-07-12}}</ref> She had previously said on Radio 1 that she loved [[Chris Moyles]], but then remarked, after being invited on his show, that he was too self-obsessed in the presence of his guests.  

Rapper [[Example (rapper)|Example]] has recorded a new version of "Smile", called "Vile", which is an [[answer song]] written from the perspective of the ex-boyfriend (although he was never actually Allen's boyfriend<ref>{{ cite web | url = http://forums.lilyallenmusic.com/viewtopic.php?p=10916#10916 | title = Untitled Forum Post | accessdate = 2006-07-28 | last = Allen | first = Lily}}</ref>). Both songs were then parodied by [[Chris Moyles]] on his Radio 1 breakfast show in a song called "Piles". Rapper [[Sway DaSafo]] has recorded an [[answer song]] to "LDN". When asked by the ''[[NME]]'' how she would celebrate if her single got to Number 1 in the UK charts, she responded "gak" (a slang term for [[cocaine]]). She immediately said that she was joking, but the "gak" remark was widely quoted in the [[Tabloid|tabloid press]]. Allen has since apologised for the remark.<ref>{{cite news | title = Lily Allen: 'Cocaine Celebration Comment Was A Joke'. | publisher = FemaleFirst.co.uk | date = 2006-07-19 | url = http://www.femalefirst.co.uk/entertainment/207142004.htm | accessdate = 2006-08-03}}</ref><ref>{{ cite web | url = http://forums.lilyallenmusic.com/viewtopic.php?p=8453&highlight=#8453 | title = Hey Everyone | accessdate = 2006-08-03 | last = Allen | first = Lily}}</ref> She has also stated that she was a [[drug dealer]] when she was 15 years old and living in [[Ibiza]].<ref>{{cite news | title = Lily Allen admits to drug dealing. | publisher = NME.com | date = 2006-12-06 | url = http://www.nme.com/news/lily-allen/25332 | accessdate = 2007-03-08}}</ref>

She has also been called a [[Mockney]].<ref>{{cite news |url=http://arts.guardian.co.uk/reviews/story/0,,1773979,00.html |title=Lily Allen |accessdate=2007-02-24 |author=Sullivan, Caroline |date=2006-05-13 |publisher= The Guardian}}</ref>

===Reaction===
Allen has been the subject of both strong positive and negative reactions. She was included on the ''[[NME]]'' ''Cool List'' for 2006, but she complained in her blog that the treatment was "fucking patronising." She was voted the third coolest person of the year, ahead of the likes of [[Gerard Way]] (eighth) and [[Jarvis Cocker]] (fourth).<ref>{{cite web|url=http://www.drownedinsound.com/articles/1335688|title=Lily Allen: angry at "f*cking patronising" magazine|author=Mike Diver|date=2006-11-23}}</ref> She ranked "Number One Reason to Love '07"<ref>{{cite news |last= |first= |url=http://www.blender.com/guide/articles.aspx?ID=2413&src=blender_ed |title=25 Reasons to Love '07 |publisher=Blender Magazine Online |date=Jan/Feb 2007 |accessdate=2007-02-03}}</ref> and "Hottest Woman of Pop/R&B."<ref>{{cite news |last= |first= |url=http://www.blender.com/guide/articles.aspx?id=2454&src=blender_ed |title=Hottest Women of...Pop/R&B! |publisher=Blender Magazine Online |date=Jan/Feb 2007 |accessdate=2007-02-09}}</ref> in ''[[Blender magazine]]''.  On the other hand, she was also rated number 10 in ''BBC3'''s Most Annoying Person of 2006.

